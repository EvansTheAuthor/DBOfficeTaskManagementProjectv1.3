    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow=tblEmployee.getSelectedRow();
        if(selectedRow!=-1){
            String employee_id=tblEmployee.getValueAt(selectedRow, 0).toString();
            
            int confirm=JOptionPane.showConfirmDialog(this, "Apakah Anda yakin ingin menghapus karyawan ini?", "Konfirmasi Penghapusan", JOptionPane.YES_NO_OPTION);
            
            if(confirm==JOptionPane.YES_OPTION){
                try{
                    EmployeeAdmin emAdm=new EmployeeAdmin();
                    emAdm.deleteEmployee(employee_id);
                    loadEmployees();
                }catch(SQLException ex){
                    JOptionPane.showConfirmDialog(this, "Error saat menghapus karyawan "+ex, "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        }else{
            JOptionPane.showMessageDialog(this, "Pilih Karyawan yang ingin dihapus!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

