    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int selectedRow=tblEmployee.getSelectedRow();
        if(selectedRow!=-1){
            String employeeId=tblEmployee.getValueAt(selectedRow, 0).toString();
            String employeeName = tblEmployee.getValueAt(selectedRow, 1).toString();  // Ambil nama karyawan dari kolom kedua
            String employeePosition = tblEmployee.getValueAt(selectedRow, 2).toString();  // Ambil posisi dari kolom ketiga
            String employeeAddress = tblEmployee.getValueAt(selectedRow, 3).toString();  // Ambil alamat dari kolom keempat
            String employeeEmail = tblEmployee.getValueAt(selectedRow, 4).toString();  // Ambil email dari kolom kelima
            String employeePhone = tblEmployee.getValueAt(selectedRow, 5).toString();
            EmployeeUpdate emp=new EmployeeUpdate(employeeId, employeeName,
                    employeePosition, employeeAddress,
                    employeeEmail, employeePhone);
        emp.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(this, "Pilih Karyawan yang ingin diperbarui!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

